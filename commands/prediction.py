def get_random_vocal_prediction():
    """Получить случайное предсказание"""
    try:
        return random.choice(prediction_responses)
    except Exception as e:
        print(f"Ошибка при получении предсказания: {e}")
        return "🔮 Не удалось получить предсказание."

async def prediction_command(update: Update, context: ContextTypes.DEFAULT_TYPE):
    """Обработчик команды /prediction"""
    prediction = get_random_vocal_prediction()
    
    try:
        # Путь к файлу с картинкой
        image_path = 'images/prediction.png'
        photo = build_binary_stream(image_path)

        if photo:
            await update.message.reply_photo(
                photo,
                caption=f"🧙‍♂️ Вокальный предсказатель:\n{prediction}",
                reply_to_message_id=update.message.message_id
            )
            print(f"Картинка {image_path} отправлена с предсказанием как ответ.")
        else:
            # Если файл не найден, отправляем только текст предсказания как ответ
            print(f"Файл картинки {image_path} не найден. Отправляю только текст как ответ.")
            await update.message.reply_text(f"🧙‍♂️ Вокальный предсказатель:\n{prediction}")

    except Exception as e:
        print(f"Ошибка при отправке картинки {image_path} или предсказания: {e}")
        await update.message.reply_text(f"Произошла ошибка при получении предсказания: {e}")
